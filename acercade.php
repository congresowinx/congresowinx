  <p><b>Prólogo</b></p>
        <br>
        <p>El Decimocuarto Congreso Internacional sobre la Enseñanza y Aplicación de las Matemáticas tiene el propósito de reunir a investigadores y profesores de reconocida trayectoria profesional relacionados con la enseñanza y aplicación de las matemáticas ahora con el panorama actual en los ambientes virtuales y a partir de la nueva normalidad a la que nos enfrentamos al regreso de nuestras actividades de manera híbrida. El objetivo es intercambiar experiencias y conocimientos que permitan mejorar la enseñanza y aprendizaje de las matemáticas en todos los niveles educativos, conjuntando esfuerzos en beneficio de los estudiantes. Durante el congreso se podrá analizar, discutir y difundir el estado del conocimiento de las matemáticas a nivel nacional o internacional, vinculando una visión conjunta de los participantes proponiendo acciones e intereses comunes, así como la investigación de calidad que interactúe en la actualización y perfeccionamiento del desarrollo científico, tecnológico y social.</p>
